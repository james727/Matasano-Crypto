def create_block(text):
    c1 = [ord(x) for x in text[0:4]]
    c2 = [ord(x) for x in text[4:8]]
    c3 = [ord(x) for x in text[8:12]]
    c4 = [ord(x) for x in text[12:16]]
    return [c1,c2,c3,c4]

def reformulate_block(block):
    output = ""
    for column in block:
        chars = "".join([chr(x) for x in column])
        output+= chars
    return output

def ecb_encrypt(plain_text, key):
    key_block = create_block(key)
    encrypted_text = ""
    num_iters = len(plain_text)//16
    for i in range(num_iters):
        cipher_text = plain_text[0+16*i:16+16*i]
        cipher_block = create_block(cipher_text)
        encrypted_block = aes_encrypt(cipher_block, key_block)
        encrypted_text+=reformulate_block(encrypted_block)
    return encrypted_text

def ecb_decrypt(cipher, key):
    key_block = create_block(key)
    decrypted_text = ""
