def aes_encrypt(state_block, key):
    keys = get_key_schedule(key)
    new_state = add_round_key(state_block, keys[0])
    for i in range(1,10):
        new_state = encrypt_state_round19(new_state, keys[i])
    final_state = encrypt_state_round10(new_state,keys[10])
    return final_state

def aes_decrypt(state_block, key):
    keys = get_key_schedule(key)
    new_state = decrypt_state_round10(state_block, keys[10])
    for i in range(-9,0,1):
        new_state = decrypt_state_round19(new_state, keys[-1*i])
    final_state = add_round_key(new_state, keys[0])
