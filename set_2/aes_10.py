def r_con_conversion(word1, word2, r_constant):
    output = []
    for i in range(len(word1)):
        if i == 0:
            output.append(word1[i]^word2[i]^r_constant)
        else:
            output.append(word1[i]^word2[i])
    return output

def shift_rows(state_block):
    # row 1 stays the same
    row1 = [x[0] for x in state_block]
    row2 = [x[1] for x in state_block]
    row2_rot = row2[1:]+[row2[0]]
    row3 = [x[2] for x in state_block]
    row3_rot = row3[2:]+row3[:2]
    row4 = [x[3] for x in state_block]
    row4_rot = [row4[3]]+row4[0:3]
    output = []
    for i in range(4):
        column = [row1[i], row2_rot[i], row3_rot[i], row4_rot[i]]
        output.append(column)
    return output

