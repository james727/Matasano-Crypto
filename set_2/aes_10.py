def add_round_key(starting_state, key_block):
    output = []
    for i in range(4):
        state = starting_state[i]
        key = key_block[i]
        output.append([x^y for x,y in zip(state,key)])
    return output

def encrypt_state_round19(starting_state, key_block):
    state_sub_bytes = map(sub_bytes, starting_state)
    state_shift_rows = shift_rows(state_sub_bytes)
    state_shift_columns = mix_columns(state_shift_rows)
    state_add_sub = add_round_key(state_shift_columns, key_block)
    return state_add_sub

def decrypt_state_round19(starting_state, key_block):
    state_pre_sub = add_round_key(starting_state, key_block)
    state_unmix_columns = unmix_columns(state_pre_sub)
    state_unshift_rows = unshift_rows(state_unmix_columns)
    state_unsub_bytes = map(unsub_bytes, state_unshift_rows)
    return state_unsub_bytes

