def cbc_encrypt(plain_text,key,initialization_vector):
    key_block = create_block(key)
    encrypted_text = ""
    num_iters = len(plain_text)//16
    prior_block = create_block(initialization_vector)
    for i in range(num_iters):
        plain_text_chunk = plain_text[0+16*i:16+16*i]
        cipher_block = create_block(plain_text_chunk)
        xored_block = add_round_key(cipher_block, prior_block)
        encrypted_block = aes_encrypt(xored_block, key_block)
        prior_block = encrypted_block
        encrypted_text+=reformulate_block(encrypted_block)
    return encrypted_text

def cbc_decrypt(cipher, key, initialization_vector):
    key_block = create_block(key)
    decrypted_text = ""
    num_iters = len(cipher)//16
    for i in range(num_iters):
        cipher_text = cipher[len(cipher)-16*(i+1):len(cipher)-16*i]
        if i != num_iters-1:
            prior_block = create_block(cipher[len(cipher)-16*(i+2):len(cipher)-16*(i+1)])
        else:
            prior_block = create_block(initialization_vector)
        cipher_block = create_block(cipher_text)
        aes_decrypted_block = aes_decrypt(cipher_block, key_block)
        decrypted_block = add_round_key(aes_decrypted_block, prior_block)
        decrypted_text=reformulate_block(decrypted_block)+decrypted_text
    return decrypted_text

